		const MappingEntry* findMapping(std::span<const MappingEntry> table, uint16_t key) noexcept {
			const auto entry = std::lower_bound(table.begin(), table.end(), key, [](const MappingEntry& candidate, uint16_t needle) {
				return candidate.key < needle;
			});
			return entry != table.end() && entry->key == key ? &*entry : nullptr;
		}

		const CollisionEntry* findCollision(uint16_t client_id) noexcept {
			const auto collision = std::lower_bound(kCollisions.begin(), kCollisions.end(), client_id, [](const CollisionEntry& candidate, uint16_t needle) {
				return candidate.client_id < needle;
			});
			return collision != kCollisions.end() && collision->client_id == client_id ? &*collision : nullptr;
		}

		std::span<const uint16_t> candidatesFor(const MappingEntry& reverse) noexcept {
			if (const auto* collision = findCollision(reverse.key)) {
				return { kAmbiguousCandidates.data() + collision->offset, collision->count };
			}
			return { &reverse.value, 1 };
		}

