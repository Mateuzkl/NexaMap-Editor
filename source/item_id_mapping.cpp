	Result convert(uint16_t id, Direction direction) noexcept {
		return direction == Direction::ServerToClient ? serverToClient(id) : clientToServer(id);
	}

	Result serverToClient(uint16_t server_id) noexcept {
		const auto* mapping = findMapping(kForwardMappings, server_id);
		if (!mapping) {
			return { server_id, server_id, false, false, {} };
		}
		const auto* reverse = findMapping(kReverseMappings, mapping->value);
		const auto candidates = reverse ? candidatesFor(*reverse) : std::span<const uint16_t>();
		return { server_id, mapping->value, true, candidates.size() > 1, candidates };
	}

	Result clientToServer(uint16_t client_id) noexcept {
		const auto* mapping = findMapping(kReverseMappings, client_id);
		if (!mapping) {
			return { client_id, client_id, false, false, {} };
		}

		const auto candidates = candidatesFor(*mapping);
		return { client_id, mapping->value, true, candidates.size() > 1, candidates };
	}

