		template <std::size_t RunCount>
		consteval std::size_t expandedCount(const std::array<MappingRun, RunCount>& runs) {
			std::size_t count = 0;
			for (const auto& run : runs) {
				count += run.length;
			}
			return count;
		}

		template <std::size_t RunCount>
		consteval bool validRuns(const std::array<MappingRun, RunCount>& runs) {
			uint32_t previous_last = 0;
			bool first = true;
			for (const auto& run : runs) {
				if (run.length == 0 || static_cast<uint32_t>(run.key_first) + run.length - 1 > std::numeric_limits<uint16_t>::max() || static_cast<uint32_t>(run.value_first) + run.length - 1 > std::numeric_limits<uint16_t>::max()) {
					return false;
				}
				if (!first && previous_last >= run.key_first) {
					return false;
				}
				previous_last = static_cast<uint32_t>(run.key_first) + run.length - 1;
				first = false;
			}
			return true;
		}

		template <std::size_t EntryCount, std::size_t RunCount>
		consteval std::array<MappingEntry, EntryCount> expandRuns(const std::array<MappingRun, RunCount>& runs) {
			std::array<MappingEntry, EntryCount> entries {};
			std::size_t position = 0;
			for (const auto& run : runs) {
				for (uint32_t offset = 0; offset < run.length; ++offset) {
					entries[position++] = {
						static_cast<uint16_t>(run.key_first + offset),
						static_cast<uint16_t>(run.value_first + offset),
					};
				}
			}
			return entries;
		}

