	bool validateTables() noexcept {
		for (std::size_t index = 1; index < kForwardMappings.size(); ++index) {
			if (kForwardMappings[index - 1].key >= kForwardMappings[index].key) {
				return false;
			}
		}
		for (std::size_t index = 1; index < kReverseMappings.size(); ++index) {
			if (kReverseMappings[index - 1].key >= kReverseMappings[index].key) {
				return false;
			}
		}

		std::size_t expected_offset = 0;
		for (std::size_t index = 0; index < kCollisions.size(); ++index) {
			const auto& collision = kCollisions[index];
			if ((index > 0 && kCollisions[index - 1].client_id >= collision.client_id) || collision.count < 2 || collision.offset != expected_offset || collision.offset + collision.count > kAmbiguousCandidates.size()) {
				return false;
			}

			const auto candidates = std::span(kAmbiguousCandidates).subspan(collision.offset, collision.count);
			if (!std::is_sorted(candidates.begin(), candidates.end()) || std::adjacent_find(candidates.begin(), candidates.end()) != candidates.end()) {
				return false;
			}

			const auto* reverse = findMapping(kReverseMappings, collision.client_id);
			if (!reverse || reverse->value != candidates.front()) {
				return false;
			}
			for (const auto server_id : candidates) {
				const auto* forward = findMapping(kForwardMappings, server_id);
				if (!forward || forward->value != collision.client_id) {
					return false;
				}
			}
			expected_offset += collision.count;
		}
		if (expected_offset != kAmbiguousCandidates.size()) {
			return false;
		}

		for (const auto& forward : kForwardMappings) {
			const auto* reverse = findMapping(kReverseMappings, forward.value);
			if (!reverse) {
				return false;
			}
			const auto candidates = candidatesFor(*reverse);
			if (!std::binary_search(candidates.begin(), candidates.end(), forward.key)) {
				return false;
			}
		}
		for (const auto& reverse : kReverseMappings) {
			const auto* forward = findMapping(kForwardMappings, reverse.value);
			if (!forward || forward->value != reverse.key) {
				return false;
			}
		}
		return true;
	}

